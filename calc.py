
def add(a, b):
    """Add function"""
    return a + b


def subtract(a, b):
    """Add function"""
    return a - b


def multiply(a, b):
    """Add function"""
    return a * b


def divide(a, b):
    if b == 0:
        raise ValueError("Can not divide by zero")
    """Add function"""
    return a / b



